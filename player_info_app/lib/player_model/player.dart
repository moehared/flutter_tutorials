class Player {
  final String id;
  final String name;
  final int age;
  final double salary;
  final String position;
  final String currentTeam;
  final String currentTeamLogo;
  final String playerImage;
  final String playerDescription;
  final String countryFlag;
  final String countryName;

  const Player({
    this.id,
    this.name,
    this.currentTeamLogo,
    this.age,
    this.salary,
    this.position,
    this.currentTeam,
    this.playerImage,
    this.playerDescription,
    this.countryFlag,
    this.countryName,
  });
}
